import turtle
s=turtle.Turtle()
s.speed(1)
s.pensize(10)
s.color("yellow")
s.pencolor("black")
s.begin_fill()
s.circle(100)
s.end_fill()
s.penup()
s.setpos(50,120)
s.pendown()
s.color("black")
s.begin_fill()
s.circle(10)
s.end_fill()
s.penup()
s.setpos(-40,120)
s.pendown()
s.color("black")
s.begin_fill()
s.circle(10)
s.end_fill()
s.penup()
s.setpos(-45,50)
s.pendown()
s.right(45)
s.forward(1)
for x in range(100):
    s.forward(1)
    s.left(1)
turtle.done